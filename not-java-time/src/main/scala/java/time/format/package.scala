package java.time.format

import java.time.unimplemented

trait DateTimeFormatter
object DateTimeFormatter {
  def ISO_LOCAL_DATE: DateTimeFormatter = unimplemented
  def ISO_LOCAL_DATE_TIME: DateTimeFormatter = unimplemented
  def ISO_LOCAL_TIME: DateTimeFormatter = unimplemented
  def ISO_OFFSET_DATE_TIME: DateTimeFormatter = unimplemented
  def ISO_OFFSET_TIME: DateTimeFormatter = unimplemented
  def ISO_ZONED_DATE_TIME: DateTimeFormatter = unimplemented
}
